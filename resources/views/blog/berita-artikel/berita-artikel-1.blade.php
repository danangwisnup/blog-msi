                      </footer>
                    </blockquote>
                    <p>
                      Dalam rangka mendukung pencapaian Strategi Nasioanal Pengendalian Tuberkulosis di Indonesia, Yasayasan Mentari Indonesia Kabupaten Klaten kembali menyelenggarakan kegiatan Pertemuaan Dukungan Pasien TB-RO pada Rabu,6 Maret 2024. Bekerja sama dengan RSUP dr.Soeradji dalam memberikan dukungan kepada pasien TB-RO Kabupaten Klaten. Dalam kegiatan tersebut,terdapat 3 pasien yang berhasil menyelesaikan pengobatan dan mendapatkan sertifikat sebagai apresiasi karena telah menyelesaikan pengobatan. 
                    </p>
